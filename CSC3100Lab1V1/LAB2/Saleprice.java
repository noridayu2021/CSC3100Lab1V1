//QUESTION	: The sale price of an item given an original price and a percentage discount. 
public class Saleprice {
	public static void main(String[] args) {
		int originalprice = 10;
		double discountpercentage = 50;
		double saleprice = originalprice -(originalprice*(discountpercentage/100));
		System.out.println("Sale price is"+saleprice);
		
		
	}

}

