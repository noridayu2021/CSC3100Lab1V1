public class BodyMassIndex {//This is to calculate the body mass index
	public static void main(String[]args) {
		int weight=100;
		double height=1.8;
		double bodymassindex=(weight/(height*height));//This is the formula to calculate body mass index
		
		System.out.println("the body mass index is "+bodymassindex);
	}
	

}
